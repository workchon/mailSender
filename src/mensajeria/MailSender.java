    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        //llenado de los datos
        String Destinatario = txtCorreoDes.getText() + cbxServidorDes.getItemAt(cbxServidorDes.getSelectedIndex());
        String Remitente = txtCorreoRem.getText() + cbxServidorRem.getItemAt(cbxServidorRem.getSelectedIndex());
        String Mensaje = txtMensaje.getText();
        String Password="";
        for (int i = 0; i < pswContraRem.getPassword().length; i++) {
            Password +=Character.toString(pswContraRem.getPassword()[i]);
        }
        String Propiedad = propiedades.get(cbxServidorRem.getSelectedIndex()).toString();
        String Asunto = txtAsunto.getText();
        c.corr.llenarDatos(Destinatario, Remitente, Mensaje, Password, Propiedad, Asunto);
        //envio del mensaje
        c.Enviar(Propiedad);
    }//GEN-LAST:event_btnEnviarActionPerformed

