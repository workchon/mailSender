    public void Refreshlist() throws IOException, SQLException, FileNotFoundException, ClassNotFoundException
    {
        servidores=new ArrayList();
        try {
              for (Object str : c.getServidor()) {
                servidores.add(str.toString());
            }  
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(MailSender.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,ex);
        }
        for (Object servidore : servidores) {
            
        }
        fillCombos();
    }
    public void fillCombos()
    {
        cbxServidorDes.removeAllItems();
        cbxServidorRem.removeAllItems();
        for (Object servidor : servidores) {
            cbxServidorDes.addItem(servidor.toString().trim());
            cbxServidorRem.addItem(servidor.toString().trim());
        }
    }
